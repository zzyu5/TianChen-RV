static bool bits_equal(float a, float b) {
  return memcmp(&a, &b, sizeof(float)) == 0;
}

static uint32_t rng = 0x1234567u;
static uint32_t xr() { rng ^= rng << 13; rng ^= rng >> 17; rng ^= rng << 5; return rng; }

// a half value used to make the reconstructed super-block scale non-degenerate. We
// pick fp16 bits for a scale around 1.0..4.0 and SPREAD them into the scales[] high
// nibbles exactly as ggml's encoder does (the inverse of the union read).
static void set_packed_scale(block_iq1_m *X, uint16_t fp16bits) {
  // scales[] is 8 uint8 = 4 uint16 words sc[0..3]. The reconstruction reads:
  //   d = (sc0>>12) | ((sc1>>8)&0xf0) | ((sc2>>4)&0xf00) | (sc3&0xf000)
  // so we must place fp16 nibble n0 (bits 0..3) into sc0 bits 12..15, n1 (bits 4..7)
  // into sc1 bits 12..15, n2 (bits 8..11) into sc2 bits 12..15, n3 (bits 12..15) into
  // sc3 bits 12..15. The low 12 bits of each word carry the per-sub-block scales (set
  // separately); here we only OR in the high nibble.
  uint16_t *sc = (uint16_t *)X->scales;
  uint16_t n0 = (fp16bits >> 0) & 0xf;
  uint16_t n1 = (fp16bits >> 4) & 0xf;
  uint16_t n2 = (fp16bits >> 8) & 0xf;
  uint16_t n3 = (fp16bits >> 12) & 0xf;
  sc[0] = (uint16_t)((sc[0] & 0x0fff) | (n0 << 12));
  sc[1] = (uint16_t)((sc[1] & 0x0fff) | (n1 << 12));
  sc[2] = (uint16_t)((sc[2] & 0x0fff) | (n2 << 12));
  sc[3] = (uint16_t)((sc[3] & 0x0fff) | (n3 << 12));
}

// fp16 bit patterns for a few well-behaved positive scales (lossless on the board).
static const uint16_t kScaleBits[] = {
  0x3c00, // 1.0
  0x4000, // 2.0
  0x3800, // 0.5
  0x3e00, // 1.5
  0x4400, // 4.0
  0x3400, // 0.25
};

static void fill_block(block_iq1_m *X, block_q8_K *Y, int idxMode) {
  // qs: random index bytes
  for (int j = 0; j < QK_K/8; ++j) X->qs[j] = (uint8_t)xr();
  // qh: random (carries index-high 3 bits @0..2/4..6 + delta signs @3/7)
  for (int j = 0; j < QK_K/16; ++j) X->qh[j] = (uint8_t)xr();
  // scales: random low 12 bits (per-sub-block 3-bit scales), high nibble set below
  uint16_t *sc = (uint16_t *)X->scales;
  for (int w = 0; w < 4; ++w) sc[w] = (uint16_t)(xr() & 0x0fff);
  set_packed_scale(X, kScaleBits[xr() % (sizeof(kScaleBits)/sizeof(kScaleBits[0]))]);
  // q8_K activation
  Y->d = 0.01f + (float)(xr() % 1000) * 0.001f;
  for (int j = 0; j < QK_K; ++j) {
    int v;
    if (idxMode == 1) v = 127;
    else if (idxMode == 2) v = ((xr() & 1) ? 127 : -127);
    else v = (int)(xr() % 255) - 127;
    Y->qs[j] = (int8_t)v;
  }
  // bsums (ggml maintains them; iq1_m does not read them, but set them for realism)
  for (int ib = 0; ib < QK_K/16; ++ib) {
    int sum = 0;
    for (int j = 0; j < 16; ++j) sum += Y->qs[ib*16 + j];
    Y->bsums[ib] = (int16_t)sum;
  }
}

// edge: drive qs=0xff + qh group-index fields=7 -> grid index 2047; toggle per-sub-block
// scale fields to extremes (0 -> ls=1, 7 -> ls=15) and the 4 delta signs.
static int fill_edge_block(block_iq1_m *X, block_q8_K *Y, int *hit2047, int rep) {
  for (int j = 0; j < QK_K/8; ++j) X->qs[j] = 0xff;       // index low 8 bits all 1
  // qh: set index-high fields to 7 (bits 0..2 and 4..6) + toggle delta bits 3,7.
  for (int j = 0; j < QK_K/16; ++j) {
    uint8_t v = 0x77;  // bits 0..2 = 7, bits 4..6 = 7
    if (rep & 1) v |= 0x08;   // delta even-group sign
    if (rep & 2) v |= 0x80;   // delta odd-group sign
    X->qh[j] = v;
  }
  uint16_t *sc = (uint16_t *)X->scales;
  // per-sub-block 3-bit scales: alternate between 0 (ls=1) and 7 (ls=15).
  for (int w = 0; w < 4; ++w) {
    uint16_t lo = 0;
    for (int half = 0; half < 2; ++half) {
      // each word holds 2 sub-blocks * 2 halves * 3 bits = 12 bits (shifts 0,3,6,9)
    }
    // pack ls fields: sub-block even -> bits 0..5, odd -> bits 6..11.
    uint16_t field = (rep & 4) ? 0x7 : 0x0;
    lo = (uint16_t)(field | (field << 3) | (field << 6) | (field << 9));
    sc[w] = (uint16_t)(lo & 0x0fff);
  }
  set_packed_scale(X, kScaleBits[rep % (sizeof(kScaleBits)/sizeof(kScaleBits[0]))]);
  Y->d = 0.5f;
  int maxIdx = 0;
  for (int j = 0; j < QK_K; ++j) Y->qs[j] = (int8_t)(((rep + j) & 1) ? 127 : -127);
  for (int ib = 0; ib < QK_K/16; ++ib) {
    int sum = 0; for (int j = 0; j < 16; ++j) sum += Y->qs[ib*16 + j];
    Y->bsums[ib] = (int16_t)sum;
  }
  // compute the max grid index this block reaches (qs=0xff + qh 3-bit fields=7).
  const uint8_t *qs = X->qs; const uint8_t *qh = X->qh;
  for (int ib = 0; ib < QK_K/32; ++ib) {
    for (int l = 0; l < 4; ++l) {
      int idx = qs[l] | (((uint16_t)qh[l/2] << (8 - 4*(l%2))) & 0x700);
      if (idx > maxIdx) maxIdx = idx;
      if (idx == 2047) *hit2047 = 1;
    }
    qs += 4; qh += 2;
  }
  return maxIdx;
}

