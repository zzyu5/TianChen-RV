// A WRONG grid: every grid byte +1 (a legal-but-different ternary table) for the
// negative control.
static uint64_t iq1s_grid_wrong[2048];
static void make_wrong_grid() {
  for (int i = 0; i < 2048; ++i) {
    uint64_t w = iq1s_grid[i], out = 0;
    for (int b = 0; b < 8; ++b) {
      uint8_t byte = (uint8_t)((w >> (8*b)) & 0xff);
      out |= (uint64_t)((uint8_t)(byte + 1)) << (8*b);
    }
    iq1s_grid_wrong[i] = out;
  }
}

struct RefKnobs {
  const uint64_t *grid;   // grid table (real or wrong)
  bool dropScalePlusOne;  // wrong-scale: omit the +1 in ls1/ls2
  bool dropDelta;         // wrong-delta: omit the delta term (sumi2 contribution)
  bool wrongRecon;        // wrong iq1m_scale reconstruction (perturb a nibble shift)
};

// ggml's VERBATIM iq1_m _generic dot (quants.c:1142-1201) with the iq1m_scale_t union.
static void ref_iq1_m(int n, float *s, const block_iq1_m *x, const block_q8_K *y,
                      RefKnobs k) {
  const int nb = n / QK_K;
  iq1m_scale_t scale;
  int sum1[2], sum2[2], delta[4];
  float sumf = 0;
  for (int i = 0; i < nb; ++i) {
    const int8_t  *q8 = y[i].qs;
    const uint8_t *qs = x[i].qs;
    const uint8_t *qh = x[i].qh;
    const uint16_t *sc = (const uint16_t *)x[i].scales;

    if (k.wrongRecon)
      // perturb the reconstruction: take sc[1] high nibble from the WRONG shift.
      scale.u16 = (sc[0] >> 12) | ((sc[1] >> 4) & 0x00f0) | ((sc[2] >> 4) & 0x0f00) | (sc[3] & 0xf000);
    else
      scale.u16 = (sc[0] >> 12) | ((sc[1] >> 8) & 0x00f0) | ((sc[2] >> 4) & 0x0f00) | (sc[3] & 0xf000);

    int sumi1 = 0, sumi2 = 0;
    for (int ib = 0; ib < QK_K/32; ++ib) {
      delta[0] = qh[0] & 0x08 ? -1 : 1;
      delta[1] = qh[0] & 0x80 ? -1 : 1;
      delta[2] = qh[1] & 0x08 ? -1 : 1;
      delta[3] = qh[1] & 0x80 ? -1 : 1;
      sum1[0] = sum1[1] = sum2[0] = sum2[1] = 0;
      for (int l = 0; l < 4; ++l) {
        const int8_t *grid = (const int8_t *)(k.grid + (qs[l] | (((uint16_t)qh[l/2] << (8 - 4*(l%2))) & 0x700)));
        int lsum1 = 0, lsum2 = 0;
        for (int j = 0; j < 8; ++j) {
          lsum1 += q8[j] * grid[j];
          lsum2 += q8[j];
        }
        q8 += 8;
        sum1[l/2] += lsum1;
        sum2[l/2] += lsum2 * delta[l];
      }
      const int plus = k.dropScalePlusOne ? 0 : 1;
      const int ls1 = 2*((sc[ib/2] >> (6*(ib%2)+0)) & 0x7) + plus;
      const int ls2 = 2*((sc[ib/2] >> (6*(ib%2)+3)) & 0x7) + plus;
      sumi1 += sum1[0] * ls1 + sum1[1] * ls2;
      sumi2 += sum2[0] * ls1 + sum2[1] * ls2;
      qs += 4;
      qh += 2;
    }
    const float d = fp16_bits_to_f32(scale.u16) * y[i].d;
    if (k.dropDelta)
      sumf += d * ((float)sumi1);
    else
      sumf += d * ((float)sumi1 + IQ1M_DELTA * (float)sumi2);
  }
  *s = sumf;
}

