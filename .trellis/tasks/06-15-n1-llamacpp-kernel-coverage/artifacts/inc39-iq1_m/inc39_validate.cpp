#define QK_K 256
#define IQ1M_DELTA 0.125f
static const int IQ1M_STRIDE = 56;            // sizeof block_iq1_m = 32+16+8 (NO d)
static const int Q8K_STRIDE = 292;            // sizeof block_q8_K

// ---- block formats (ggml-common.h, QK_K = 256) -------------------------------
//   block_iq1_m (56 bytes): qs[32] @0 | qh[16] (uint8) @32 | scales[8] (uint8) @48
//   block_q8_K (292 bytes):  d(fp32) @0 | qs[256] @4 | bsums[16] @260  (iq1_m reads NO bsums)
typedef struct {
  uint8_t qs[QK_K/8];
  uint8_t qh[QK_K/16];
  uint8_t scales[QK_K/32];
} block_iq1_m;
typedef struct { float d; int8_t qs[QK_K]; int16_t bsums[QK_K/16]; } block_q8_K;

// The packed iq1m_scale_t union (ggml-common.h).
typedef union { _Float16 f16; uint16_t u16; } iq1m_scale_t;

static inline float fp16_bits_to_f32(uint16_t bits) {
  iq1m_scale_t s; s.u16 = bits; return (float)s.f16;
}

