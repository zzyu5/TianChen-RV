// WRONG grid for negative control 1: each entry's bytes incremented by 1 (ternary
// {-1,0,+1} -> {0,+1,+2}: corrupts the grid dot but stays a legal-but-different table).
static uint64_t iq1s_grid_wrong[2048];

// ---- board fp16 -> fp32 (mirrors riscv_compute_fp16_to_fp32, lossless) -------
static inline float fp16_to_fp32(uint16_t h) {
  _Float16 hf;
  std::memcpy(&hf, &h, sizeof(uint16_t));
  return (float)hf;
}

// ---- The kernel our compiler emitted (declared; defined in the linked .cpp) --
extern "C" void
tcrv_emitc_ggml_vec_dot_iq1_s_q8_K_kernel_ggml_vec_dot_iq1_s_q8_K(
    size_t n, float *s, const uint8_t *vx, const uint8_t *vy);

// ---- ggml _generic reference (VERBATIM quants.c:1099-1140, parameterized so the
// negative controls can swap ONE mechanism: the grid table, the scale +1, or the
// delta term) -----------------------------------------------------------------------
struct RefKnobs {
  const uint64_t *grid;     // grid table (real or wrong)
  bool dropScalePlusOne;    // wrong-scale: drop the +1 in 2*nibble+1
  bool dropDelta;           // drop the entire delta term (sumi1 contribution)
};

static void ref_iq1_s(int n, float *s, const void *vx, const void *vy,
                      const RefKnobs &k) {
  const block_iq1_s *x = (const block_iq1_s *)vx;
  const block_q8_K *y = (const block_q8_K *)vy;
  const int nb = n / QK_K;
  float sumf = 0.f;
  for (int i = 0; i < nb; ++i) {
    const int8_t   *q8 = y[i].qs;
    const uint8_t  *qs = x[i].qs;
    const uint16_t *qh = x[i].qh;
    int sumi = 0, sumi1 = 0;
    for (int ib = 0; ib < QK_K/32; ++ib) {
      const int ls = k.dropScalePlusOne ? (2*((qh[ib] >> 12) & 7))
                                        : (2*((qh[ib] >> 12) & 7) + 1);
      const int delta = (qh[ib] & 0x8000) ? -1 : 1;
      int lsum = 0;
      for (int l = 0; l < 4; ++l) {
        const int8_t *grid =
            (const int8_t *)(k.grid + (qs[l] | (((qh[ib] >> 3*l) & 7) << 8)));
        for (int j = 0; j < 8; ++j) {
          lsum += q8[j] * grid[j];
        }
        q8 += 8;
      }
      sumi  += ls * lsum;
      if (!k.dropDelta)
        sumi1 += ls * delta * (y[i].bsums[2*ib+0] + y[i].bsums[2*ib+1]);
      qs += 4;
    }
    sumf += fp16_to_fp32(x[i].d) * y[i].d * (sumi + IQ1S_DELTA * sumi1);
  }
  *s = sumf;
}

// ---- deterministic PRNG ------------------------------------------------------
static uint64_t rng_state = 0x123456789abcdef0ULL;
static uint32_t xr() {
  rng_state ^= rng_state << 13;
  rng_state ^= rng_state >> 7;
  rng_state ^= rng_state << 17;
  return (uint32_t)(rng_state >> 32);
}
static uint8_t rb() { return (uint8_t)(xr() & 0xff); }

// random fp16 in a benign range (avoid NaN/Inf exponents) ----------------------
static uint16_t rand_fp16() {
  uint16_t exp = (uint16_t)(9 + (xr() % 8));  // ~[2^-6, 2^1]
  uint16_t mant = (uint16_t)(xr() & 0x3ff);
  uint16_t sign = (uint16_t)(xr() & 1);
  return (uint16_t)((sign << 15) | (exp << 10) | mant);
}

// random qh word: grid-high-3-bit fields (bits 0..11), scale (bits 12..14), delta
// sign (bit 15) -- a full random uint16 exercises all of them.
static uint16_t rand_qh() { return (uint16_t)(xr() & 0xffff); }

static void fill_block(block_iq1_s *xb, block_q8_K *yb, int idxMode) {
  xb->d = rand_fp16();
  for (int j = 0; j < QK_K/8; ++j) xb->qs[j] = rb();
  for (int j = 0; j < QK_K/32; ++j) xb->qh[j] = rand_qh();
  yb->d = (fp16_to_fp32(rand_fp16()));
  for (int j = 0; j < QK_K; ++j) {
    if (idxMode == 1) yb->qs[j] = 127;          // q8 = +127
    else if (idxMode == 2) yb->qs[j] = (j & 1) ? 127 : -127;  // q8 = +/-127
    else yb->qs[j] = (int8_t)rb();
  }
  // bsums = exact per-16-element sums of q8 (so the delta term matches the real
  // activation contract, exactly as ggml's quantize fills them).
  for (int g = 0; g < QK_K/16; ++g) {
    int s = 0;
    for (int j = 0; j < 16; ++j) s += yb->qs[g*16 + j];
    yb->bsums[g] = (int16_t)s;
  }
}

// edge: drive the 11-bit index to 2047 (qs byte 0xff + qh 3-bit field 7 for that
// group), the scale extremes (qh scale field 0 -> ls=1; field 7 -> ls=15), the delta
// sign bit set/clear, and mark whether index 2047 is hit. Returns max index seen.
static int fill_edge_block(block_iq1_s *xb, block_q8_K *yb, int *hit2047, int rsel) {
  xb->d = rand_fp16();
  for (int j = 0; j < QK_K/8; ++j) xb->qs[j] = 0xff;  // low 8 bits = 255
  // qh: all 4 group fields = 7 (bits 0..11 all set), scale field (12..14) = 7 -> ls=15,
  // delta sign (bit 15) toggled by rsel. (0x7000|0x0fff = 0x7fff; |0x8000 if rsel.)
  uint16_t qhw = (uint16_t)(0x7fff | ((rsel & 1) ? 0x8000 : 0));
  for (int j = 0; j < QK_K/32; ++j) xb->qh[j] = qhw;
  // one sub-block forced to scale field 0 -> ls=1 (still index 2047 via the group fields)
  xb->qh[0] = (uint16_t)((qhw & 0x8fff));  // clear bits 12..14 -> scale field 0
  yb->d = fp16_to_fp32(rand_fp16());
  for (int j = 0; j < QK_K; ++j) yb->qs[j] = (int8_t)rb();
  for (int g = 0; g < QK_K/16; ++g) {
    int s = 0;
    for (int j = 0; j < 16; ++j) s += yb->qs[g*16 + j];
    yb->bsums[g] = (int16_t)s;
  }
  int maxIdx = 0;
  *hit2047 = 0;
  const uint8_t *qs = xb->qs;
  const uint16_t *qh = xb->qh;
  for (int ib = 0; ib < QK_K/32; ++ib) {
    for (int l = 0; l < 4; ++l) {
      int idx = qs[l] | (((qh[ib] >> 3*l) & 7) << 8);
      if (idx > maxIdx) maxIdx = idx;
      if (idx == 2047) *hit2047 = 1;
    }
    qs += 4;
  }
  return maxIdx;
}

static int bits_equal(float a, float b) {
  uint32_t ua, ub;
  std::memcpy(&ua, &a, 4);
  std::memcpy(&ub, &b, 4);
  return ua == ub;
}

