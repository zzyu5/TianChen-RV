// 2*((qh[ib]>>12)&7)+1, delta = qh[ib]&0x8000?-1:1, grid = (int8_t*)(iq1s_grid +
// (qs[l] | (((qh[ib]>>3l)&7)<<8))), lsum += q8[j]*grid[j], sumi += ls*lsum, sumi1 +=
// ls*delta*(bsums[2ib]+bsums[2ib+1]), sumf += d*(sumi + IQ1S_DELTA*sumi1), *s = sumf.
// FP16->FP32 mirrors the board's scalar _Float16 widening (lossless). Whole TU built
// with the same -ffp-contract.
//
