//
// iq1_s is the TERNARY class -- the last common ggml dot kernel:
//   (a) a 2048-entry TERNARY grid (iq1s_grid[2048], each uint64 = 8 values in {0,+1,
//       -1}); byte-viewed as int8 the 0xff bytes read as -1 (the grid IS the signed
//       value -- NO sign plane). The index is 11-bit: idx = qs[ib*4+l] |
//       (((qh[ib] >> (3*l)) & 7) << 8), range to 2047.
//   (b) the per-sub-block scale lives in the uint16 qh word: ls = 2*((qh[ib]>>12)&7)+1.
//   (c) the per-block DELTA term (NEW): delta = (qh[ib] & 0x8000) ? -1 : 1, folded via
